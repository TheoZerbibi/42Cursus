#include <stdio.h>
#include <string.h>
#include <ctype.h>
#include <stdarg.h>
#include "libft/libft.h"

void	ft_print_result(char const *s)
{
	int		len;

	len = 0;
	while (s[len])
		len++;
	write(1, s, len);
}

int main(void)
{
	ft_print_result(ft_itoa(-2147483648LL));
	return (0);
}
